export const reverseCharMap = Object.entries(specialCharMap).reduce((acc, [_, { map }]) => {
  Object.entries(map).forEach(([normal, special]) => {
    acc[special] = normal;
  });
  return acc;
}, {} as Record<string, string>);

// æ£€æŸ¥æ–‡æœ¬æ˜¯å¦åŒ…å«ç‰¹æ®Šå­—ç¬¦
export const hasSpecialChars = (text: string) => {
  return text.split('').some(char => reverseCharMap[char]);
};

// å°†ç‰¹æ®Šå­—ç¬¦è½¬æ¢å›žæ™®é€šå­—ç¬¦
export const normalizeText = (text: string) => {
  return text.split('').map(char => reverseCharMap[char] || char).join('');
};

export const transformText = (text: string, style: keyof typeof specialCharMap) => {
  // å…ˆå°†æ–‡æœ¬è½¬æ¢ä¸ºæ™®é€šå­—ç¬¦
  const normalText = normalizeText(text);
  // å†åº”ç”¨æ–°çš„æ ·å¼
  const map = specialCharMap[style].map;
  return normalText.split('').map(char => map[char] || char).join('');
}; 